package ai.obvs.integration.models;

public class Pan {
  private Control id;
  private Control an;
  private Control rn;
  private Control birthDate;
  private Control signature;
  private Control photo;

  public Control getId() {
    return id;
  }

  public void setId(Control id) {
    this.id = id;
  }

  public Control getAn() {
    return an;
  }

  public void setAn(Control an) {
    this.an = an;
  }

  public Control getRn() {
    return rn;
  }

  public void setRn(Control rn) {
    this.rn = rn;
  }

  public Control getBirthDate() {
    return birthDate;
  }

  public void setBirthDate(Control birthDate) {
    this.birthDate = birthDate;
  }

  public Control getSignature() {
    return signature;
  }

  public void setSignature(Control signature) {
    this.signature = signature;
  }

  public Control getPhoto() {
    return photo;
  }

  public void setPhoto(Control photo) {
    this.photo = photo;
  }

}
